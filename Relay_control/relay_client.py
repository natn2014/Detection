    def modbus_crc(self, data):
        """Calculate modbus CRC value."""
        crc_high = 0xff
        crc_low = 0xff
        for byte in data:
            index = crc_low ^ byte
            crc_low = crc_high ^ self.CRCTableHigh[index]
            crc_high = self.CRCTableLow[index]
        return (crc_high << 8 | crc_low)

    def connect(self) -> bool:
        """Connect to the relay device."""
        try:
            if self.sock:
                return True
            self.sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            self.sock.settimeout(2.0)
            self.sock.connect((self.host, self.port))
            self.connected = True
            print(f"Connected to relay at {self.host}:{self.port}")
            return True
        except Exception as e:
            print(f"Connection failed: {e}")
            self.connected = False
            return False

    def disconnect(self):
        """Disconnect from the relay device."""
        try:
            if self.sock:
                self.sock.close()
                self.sock = None
            self.connected = False
        except Exception as e:
            print(f"Disconnect error: {e}")

    def _send_command(self, cmd: List[int]) -> Optional[bytes]:
        """Send a command and receive response."""
        try:
            if not self.sock or not self.connected:
                if not self.connect():
                    return None

            # Calculate CRC
            crc = self.modbus_crc(cmd)
            cmd.append(crc & 0xFF)
            cmd.append(crc >> 8)

            # Send command
            self.sock.send(bytearray(cmd))

            # Receive response
            response = self.sock.recv(1024)
            return response
        except (socket.timeout, BrokenPipeError, ConnectionResetError, OSError) as e:
            error_type = type(e).__name__
            print(f"Connection error ({error_type}): {e}")
            self.connected = False
            self.disconnect()
            return None
        except Exception as e:
            print(f"Command error: {e}")
            self.connected = False
            self.disconnect()
            return None

    def read_digital_inputs(self) -> Optional[List[bool]]:
        """Read all 8 digital inputs (DI1-DI8)."""
        try:
            cmd = [0x01, 0x02, 0x00, 0x00, 0x00, 0x08]
            response = self._send_command(cmd[:])
            
            if not response or len(response) < 4:
                return None

            di_status = response[3]
            # Convert byte to list of 8 booleans
            di_list = [(di_status >> i) & 1 for i in range(8)]
            return di_list
        except Exception as e:
            print(f"Error reading DI: {e}")
            return None

    def read_digital_outputs(self) -> Optional[List[bool]]:
        """Read all 8 relay channels (DO1-DO8)."""
        try:
            cmd = [0x01, 0x01, 0x00, 0x00, 0x00, 0x08]
            response = self._send_command(cmd[:])
            
            if not response or len(response) < 4:
                return None

            do_status = response[3]
            # Convert byte to list of 8 booleans
            do_list = [(do_status >> i) & 1 for i in range(8)]
            return do_list
        except Exception as e:
            print(f"Error reading DO: {e}")
            return None

    def write_digital_output(self, channel: int, value: bool) -> bool:
        """Turn a single relay channel on/off.
        
        Args:
            channel: Channel number (1-8)
            value: True to turn on, False to turn off
        """
        try:
            if channel < 1 or channel > 8:
                print(f"Invalid channel {channel}, must be 1-8")
                return False

            # Modbus function 05: Write Single Coil
            channel_idx = channel - 1  # Convert to 0-indexed
            coil_value = 0xFF00 if value else 0x0000
            
            cmd = [0x01, 0x05, 0x00, channel_idx, coil_value >> 8, coil_value & 0xFF]
            response = self._send_command(cmd[:])
            
            if not response or len(response) < 8:
                print(f"Invalid response length: {(response) if response else 0}")
                return False

            # Verify response echoes back the command (first 6 bytes before CRC)
            # Response format: [address, function, addr_h, addr_l, value_h, value_l, crc_l, crc_h]
            if (response[0] == cmd[0] and response[1] == cmd[1] and 
                response[2] == cmd[2] and response[3] == cmd[3] and
                response[4] == cmd[4] and response[5] == cmd[5]):
                print(f"Channel {channel} set to {'ON' if value else 'OFF'}")
                return True
            else:
                print(f"Response mismatch. Expected: {cmd}, got: {response[:6].hex()}")
                return False
        except Exception as e:
            print(f"Error writing DO: {e}")
            return False

    def is_connected(self) -> bool:
        """Check if connected to relay."""
        return self.connected and self.sock is not None

    def __enter__(self):
        self.connect()
        return self

    def __exit__(self, *args):
        self.disconnect()


